public class aula002p1 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		int i, n;
		n = scan.nextInt();
		
		for(i=1;i<=n; i++) {
			System.out.print(i);
		}
		i=1;
		while (i<=n){
			System.out.print(i);

		}

		
	}

}
